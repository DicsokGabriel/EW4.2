public class altele {
    public String rec1;
    public String rec2;
    public String rec3;

    public altele(String x,String y,String z)
    {
        this.rec1 = x;
        this.rec2 = y;
        this.rec3 = z;
    }

	public String getRec1() {
		return rec1;
	}

	public void setRec1(String rec1) {
		this.rec1 = rec1;
	}

	public String getRec2() {
		return rec2;
	}

	public void setRec2(String rec2) {
		this.rec2 = rec2;
	}

	public String getRec3() {
		return rec3;
	}

	public void setRec3(String rec3) {
		this.rec3 = rec3;
	}
    
    
}
